#include "libft.h"
#include <stdio.h>

void	ft_putnbr(int n)
{
	if (n > 2147483647)
		n = 2147483647;
	else if (n < -2147483648)
		n = -2147483648;
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		n = 147483648;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n *= -1;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0');
}
