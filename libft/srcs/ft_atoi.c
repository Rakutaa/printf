int		ft_atoi(const char *str)
{
	long	sum;
	long	pre;

	pre = 1;
	sum = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' ||
			*str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-')
		pre = -1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str && *str >= '0' && *str <= '9')
	{
		sum = 10 * sum + (long)(*str - '0');
		str++;
	}
	return ((int)(sum * pre));
}
